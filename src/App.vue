    const loginType = ref('user'); // 登录类型，'user' 或 'sys'
    const loginUsername = ref(''); // 登录用户名
    const loginPassword = ref(''); // 登录密码
    const loginError = ref(''); // 登录错误信息
    const loginLoading = ref(false); // 登录加载状态
    const currentUserInfo = ref(null); // 当前登录用户信息

    // 处理登录请求
    const handleLogin = async () => {
      // 重置错误信息
      loginError.value = '';
      
      // 简单的表单验证
      if (!loginUsername.value.trim()) {
        loginError.value = language.value === 'zh' ? '请输入用户名' : 'Please enter username';
        return;
      }
      if (!loginPassword.value.trim()) {
        loginError.value = language.value === 'zh' ? '请输入密码' : 'Please enter password';
        return;
      }
      
      // 设置登录加载状态
      loginLoading.value = true;
      
      try {
        // 调用登录函数
        const success = await login(loginUsername.value.trim(), loginPassword.value.trim(), loginType.value);
        
        if (success) {
          // 登录成功，检查用户是否真的登录了
          const isLoggedIn = await autoLoginCheck();
          if (isLoggedIn) {
            // 获取用户信息
            currentUserInfo.value = getCurrentUserInfo();
            console.log('登录成功，用户信息:', currentUserInfo.value);
            
            // 关闭登录对话框
            showLoginDialog.value = false;
            
            // 重置登录表单
            resetLoginForm();
          } else {
            loginError.value = language.value === 'zh' ? '登录失败，请检查您的账号密码' : 'Login failed, please check your username and password';
          }
        } else {
          loginError.value = language.value === 'zh' ? '登录失败，请检查您的账号密码' : 'Login failed, please check your username and password';
        }
      } catch (error) {
        console.error('登录过程中发生错误:', error);
        loginError.value = error.message || (language.value === 'zh' ? '登录失败，请稍后再试' : 'Login failed, please try again later');
      } finally {
        // 重置登录加载状态
        loginLoading.value = false;
      }
    };
    
    // 重置登录表单
    const resetLoginForm = () => {
      loginUsername.value = '';
      loginPassword.value = '';
      loginError.value = '';
      loginType.value = 'user';
    };

    // 初始化加载历史聊天记录
    onMounted(async () => {
      loadChatHistory();
      
      // 检查wemol平台登录状态
        const isLoggedIn = await autoLoginCheck();
        if (!isLoggedIn) {
          // 如果未登录，显示登录提示对话框
          console.log('用户未登录，显示登录提示对话框');
          showLoginDialog.value = true;
        } else {
          // 用户已登录，获取用户信息
          currentUserInfo.value = getCurrentUserInfo();
          console.log('当前登录用户信息:', currentUserInfo.value);
        }
      });

    // 加载聊天历史
    const loadChatHistory = () => {
      const savedHistory = localStorage.getItem('chatHistory');
      if (savedHistory) {
        chatHistory.value = JSON.parse(savedHistory);
      }
    };

    // 保存聊天历史
    const saveChatHistory = () => {
      // 处理ref对象，确保正确序列化
      const historyToSave = chatHistory.value.map(chat => ({
        question: chat.question,
        timestamp: chat.timestamp || new Date(),
        messages: chat.messages.map(msg => ({
          role: msg.role,
          content: msg.content,
          references: msg.references || [],
          timestamp: msg.timestamp || new Date()
        }))
      }));
      localStorage.setItem('chatHistory', JSON.stringify(historyToSave));
    };

    // 开始新对话
    const startNewChat = () => {
      currentChat.messages = [];
      userInput.value = '';
      currentChatIndex.value = -1;
      currentChatTitle.value = '';
      isInThinkingMode.value = false;
      thinkingContent.value = '';
      showThinking.value = false;
      // 如果有正在进行的请求，取消它
      if (controller.value) {
        controller.value.abort();
      }
    };

    // 加载历史对话
    const loadChat = (index) => {
      const chat = chatHistory.value[index];
      const messages = chat.messages;
      currentChat.messages = messages.map(msg => ({
        ...msg
      }));
      currentChatIndex.value = index;
      currentChatTitle.value = chat.question;
    };

    // 删除聊天记录
    const deleteChat = (index) => {
      if (confirm(language.value === 'zh' ? '确定要删除这条对话记录吗？' : 'Are you sure you want to delete this conversation?')) {
        chatHistory.value.splice(index, 1);
        saveChatHistory();
      }
    };

    // 示例问题
    const exampleQuestions = {
      singleTurn: {
        zh: [
          '影响半固体剂型溶出特征的关键因素有哪些？',
          '气体分散型剂型的优势是什么？'
        ],
        en: [
          'What are the key factors affecting the dissolution characteristics of semi-solid dosage forms?',
          'What are the advantages of gas dispersion dosage forms?'
        ]
      },
      multiTurn: {
        zh: [
          {
            title: '固体分散体技术综述',
            questions: [
              '制备固体分散体剂型的主要目的是什么？',
              '采用固体分散体技术可使药物的溶出速率提升多少？',
              '固体分散体中溶解增强的可能机制有哪些？'
            ]
          },
          {
            title: '纳米颗粒案例研究',
            questions: [
              '纳米颗粒在药物递送中的优势是什么？',
              '如何制备稳定的纳米颗粒制剂？'
            ]
          }
        ],
        en: [
          {
            title: 'Review of Solid Dispersion Technology',
            questions: [
              'What is the primary purpose of creating solid dispersion dosage forms?',
              'How much can the dissolution rate of a drug increase when using solid dispersions?',
              'What are the possible mechanisms of enhanced dissolution in solid dispersions?'
            ]
          },
          {
            title: 'Nanoparticle Case Study',
            questions: [
              'What are the advantages of nanoparticles in drug delivery?',
              'How to prepare stable nanoparticle formulations?'
            ]
          }
        ]
      }
    };

    // 切换语言
    const changeLanguage = () => {
      // 语言切换时可能需要的操作
    };

    // 发送示例问题
    const sendExampleQuestion = (question) => {
      userInput.value = question;
      sendMessage();
    };

    // 发送消息
    const sendMessage = async () => {
      if (!userInput.value.trim() || loading.value) return;

      const question = userInput.value.trim();
      
      // 在发送问题前先检查登录状态
      const isLoggedIn = checkWemolLogin();
      if (!isLoggedIn) {
        showLoginDialog.value = true;
        return;
      }

      // 添加用户消息到当前对话
      currentChat.messages.push({
        role: 'user',
        content: question,
        timestamp: new Date()
      });

      // 滚动到底部
      nextTick(() => {
        scrollToBottom();
      });

      // 清空输入框
      userInput.value = '';
      
      // 设置加载状态
      loading.value = true;
      isInThinkingMode.value = false;
      thinkingContent.value = '';
      showThinking.value = false;
      // 初始化abort controller用于取消请求
      controller.value = new AbortController();

      try {
        // 准备API请求参数
        // 使用相对路径，利用Vite的代理配置处理跨域和重定向
        const apiUrl = '/api/v1/chat/completions';
        const apiKey = 'fastgpt-mKIZmHlk5l9WSEuyMlqfqpEXEb4OzTc0nd5zFJp3DAWX0zxbGddjySq3eC';
        
        // 构建消息历史
        let messages = [];
        
        // 添加开场白（根据客户要求文档）
        messages.push({ role: 'user', content: '这是一个模拟开场白' });
        messages.push({ role: 'assistant', content: '\n我是一位制剂专家。' });
        
        // 添加用户问题
        messages.push({ role: 'user', content: question });

        // 创建响应元素 - 直接使用字符串，避免ref包装
        const responseElement = {
          role: 'assistant',
          content: '',
          references: [],
          timestamp: new Date()
        };
        
        currentChat.messages.push(responseElement);
          
        // 使用fetch API处理SSE流式响应
        const response = await fetch(apiUrl, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            // 注意：根据用户要求，这里使用相对路径，项目会嵌入到wemol平台
            // 所以不需要设置Authorization头，而是依赖cookie凭证
            'Accept': 'text/event-stream',
            'Cache-Control': 'no-cache',
            'X-Requested-With': 'XMLHttpRequest'
          },
          // 确保发送cookie凭证
          credentials: 'include',
          body: JSON.stringify({
            stream: true,
            messages: messages,
            model: 'DeepSeek-R1'
          }),
          // 使用credentials: 'include'来确保在重定向过程中也发送凭证
          credentials: 'include',
          mode: 'cors',
          signal: controller.value.signal
        });

        if (!response.ok) {
          const errorText = await response.text();
          console.error(`HTTP错误! 状态码: ${response.status}, 错误信息: ${errorText}`);
          console.error('请求URL:', apiUrl);
          console.error('请求头:', {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${apiKey}`,
            'Accept': 'text/event-stream',
            'Cache-Control': 'no-cache',
            'X-Requested-With': 'XMLHttpRequest'
          });
          console.error('请求体:', JSON.stringify({
            stream: true,
            messages: messages,
            model: 'DeepSeek-R1'
          }, null, 2));
          throw new Error(`HTTP错误! 状态码: ${response.status}, 错误信息: ${errorText}`);
        }

        const reader = response.body.getReader();
        const decoder = new TextDecoder('utf-8');
        let done = false;

        while (!done) {
          // 检查是否已取消请求
          if (controller.value.signal.aborted) {
            reader.cancel();
            done = true;
            break;
          }
          
          const { value, done: readerDone } = await reader.read();
          done = readerDone;
          
          if (value) {
            const chunk = decoder.decode(value, { stream: true });
            console.log('接收到的原始SSE数据块:', chunk);
            // 处理SSE数据格式
            const lines = chunk.split('\n');
            console.log('分割后的行数:', lines.length);
            
            for (const line of lines) {
              console.log('处理行:', line);
              if (line.startsWith('data: ')) {
                const data = line.slice(6);
                console.log('提取的数据内容:', data);
                
                if (data === '[DONE]') {
                  // 保存到历史记录
                  if (chatHistory.value.length >= 10) {
                    chatHistory.value.shift();
                  }
                  chatHistory.value.push({
                    question: question,
                    messages: [...currentChat.messages],
                    timestamp: new Date()
                  });
                  saveChatHistory();
                  // 响应完成时设置loading为false
                  loading.value = false;
                  break;
                }
                
                try {
                  const parsed = JSON.parse(data);
                  
                  // 检查不同的响应格式
                  let content = null;
                  console.log('完整的parsed响应:', JSON.stringify(parsed, null, 2));
                  
                  // 首先检查是否有任何内容，如果有就停止loading
                  const hasContent = parsed.choices && parsed.choices[0] && parsed.choices[0].delta && parsed.choices[0].delta.content;
                  const hasReasoningContent = parsed.choices && parsed.choices[0] && parsed.choices[0].delta && parsed.choices[0].delta.reasoning_content;
                  
                  if ((hasContent || hasReasoningContent) && loading.value) {
                    console.log('检测到响应内容，停止loading状态');
                    loading.value = false;
                  }
                  
                  // 如果只有reasoning_content而没有content，说明这是答案内容，不是思考过程
                  if (hasReasoningContent && !hasContent) {
                    console.log('检测到只有reasoning_content，将其作为答案内容显示');
                    isInThinkingMode.value = false; // 强制退出思考模式
                  }
                  
                  // 格式1: choices[0].delta.content (标准OpenAI格式) - 优先显示最终答案
                  if (hasContent) {
                    content = parsed.choices[0].delta.content;
                    console.log('使用格式1 - choices[0].delta.content:', content);
                  }
                  // 格式2: choices[0].delta.reasoning_content (DeepSeek-R1内容) - 仅在没有content时显示
                  else if (hasReasoningContent) {
                    const reasoningContent = parsed.choices[0].delta.reasoning_content;
                    
                    // 检查是否是思考过程的开始标记
                    if (reasoningContent === '<think>') {
                      isInThinkingMode.value = true;
                      thinkingContent.value = '';
                      showThinking.value = true;
                      console.log('开始思考过程');
                      continue; // 跳过思考开始标记
                    }
                    
                    // 检查是否是思考过程的结束标记
                    if (reasoningContent === '</think>') {
                      isInThinkingMode.value = false;
                      console.log('结束思考过程');
                      continue; // 跳过思考结束标记
                    }
                    
                    // 如果在思考模式中，收集思考内容
                    if (isInThinkingMode.value) {
                      thinkingContent.value += reasoningContent;
                      console.log('收集思考过程内容:', reasoningContent);
                      // 强制Vue重新渲染思考内容
                      nextTick(() => {
                        scrollToBottom();
                      });
                      continue;
                    }
                    
                    // 只有在非思考模式下才显示内容
                    content = reasoningContent;
                    console.log('使用格式2 - choices[0].delta.reasoning_content:', content);
                  }
                  // 格式3: choices[0].text (替代格式)
                  else if (parsed.choices && parsed.choices[0] && parsed.choices[0].text) {
                    content = parsed.choices[0].text;
                    console.log('使用格式3 - choices[0].text:', content);
                  }
                  // 格式4: content字段直接返回
                  else if (parsed.content) {
                    content = parsed.content;
                    console.log('使用格式4 - content字段:', content);
                  }
                  // 格式5: data字段
                  else if (parsed.data) {
                    content = parsed.data;
                    console.log('使用格式5 - data字段:', content);
                  }
                  
                  if (content) {
                    responseElement.content += content;
                    // 强制Vue重新渲染
                    currentChat.messages = [...currentChat.messages];
                    // 每次更新内容后滚动到底部
                    nextTick(() => {
                      scrollToBottom();
                    });
                  } else {
                    console.log('未找到有效的内容字段，响应结构:', parsed);
                  }
                  
                  // 处理可能的参考文献
                  if (parsed.choices && parsed.choices[0] && parsed.choices[0].delta && parsed.choices[0].delta.references) {
                    responseElement.references = parsed.choices[0].delta.references;
                  }
                } catch (e) {
                  console.error('解析响应失败:', e, '响应内容:', data);
                }
              }
            }
          }
        }
      } catch (error) {
        console.error('发送消息失败:', error);
        loading.value = false;
        
        // 处理不同类型的错误
        if (error.name === 'AbortError') {
          console.error('请求超时: API响应时间超过300秒');
          // 添加错误消息
          const errorMessage = {
            role: 'assistant',
            content: language.value === 'zh' 
              ? `请求超时，请检查网络连接或稍后再试。` 
              : `Request timed out. Please check your network connection or try again later.`,
            references: [],
            timestamp: new Date()
          };
          currentChat.messages.push(errorMessage);
        } else if (error.message.includes('Failed to fetch')) {
          console.error('网络请求失败:', error);
          // 添加错误消息
          const errorMessage = {
            role: 'assistant',
            content: language.value === 'zh' 
              ? `网络连接失败，请检查您的网络设置或稍后再试。` 
              : `Network connection failed. Please check your network settings or try again later.`,
            references: [],
            timestamp: new Date()
          };
          currentChat.messages.push(errorMessage);
        } else if (error.message && error.message.includes('未登录')) {
          // 检查是否是登录错误
          showLoginDialog.value = true;
        } else {
          // 添加更详细的错误消息
          const errorMessage = {
            role: 'assistant',
            content: language.value === 'zh' 
              ? `抱歉，我暂时无法为您提供答案。错误信息：${error.message}。请稍后再试。` 
              : `Sorry, I cannot provide an answer for you at the moment. Error: ${error.message}. Please try again later.`,
            references: [],
            timestamp: new Date()
          };
          currentChat.messages.push(errorMessage);
        }
      } finally {
        // 确保loading状态被正确重置
        if (loading.value) {
          loading.value = false;
        }
      }
    };

    // 取消当前请求
    const cancelRequest = () => {
      if (controller.value) {
        controller.value.abort();
        loading.value = false;
        // 移除最后添加的助手消息（如果有的话）
        if (currentChat.messages.length > 0 && 
            currentChat.messages[currentChat.messages.length - 1].role === 'assistant') {
          currentChat.messages.pop();
        }
        // 添加一条简洁的终止提示消息
        const cancelMessage = {
          role: 'assistant',
          content: language.value === 'zh' ? '对话终止' : 'Conversation terminated',
          references: [],
          timestamp: new Date()
        };
        currentChat.messages.push(cancelMessage);
      }
    };

    // 格式化消息内容（支持简单的Markdown）
    const formatMessage = (content) => {
      console.log('formatMessage接收到的内容:', content);
      if (!content || content.trim() === '') {
        console.log('内容为空，返回空字符串');
        return '';
      }
      
      // 简单的Markdown解析
      let formatted = content
        .replace(/\n/g, '<br>')
        .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
        .replace(/\*(.*?)\*/g, '<em>$1</em>')
        .replace(/`(.*?)`/g, '<code>$1</code>');
      
      console.log('格式化后的内容:', formatted);
      return formatted;
    };

    // 滚动到底部
    const scrollToBottom = () => {
      if (chatMessagesContainer.value) {
        chatMessagesContainer.value.scrollTop = chatMessagesContainer.value.scrollHeight;
      }
    };

    // 清空输入框
    const clearInput = () => {
      userInput.value = '';
    };

    // 格式化时间
    const formatTime = (date) => {
      const now = new Date(date);
      return now.toLocaleTimeString('zh-CN', {
        hour: '2-digit',
        minute: '2-digit'
      });
    };

    // 格式化日期
    const formatDate = (date) => {
      const now = new Date(date);
      const month = String(now.getMonth() + 1).padStart(2, '0');
      const day = String(now.getDate()).padStart(2, '0');
      return `${month}-${day}`;
    };

    // 复制消息
    const copyMessage = async (content) => {
      try {
        await navigator.clipboard.writeText(content);
        // 可以添加提示
      } catch (err) {
        console.error('复制失败:', err);
      }
    };

    // 复制输入内容
    const copyInput = async () => {
      try {
        await navigator.clipboard.writeText(userInput.value);
      } catch (err) {
        console.error('复制失败:', err);
      }
    };

    // 重新生成响应
    const regenerateResponse = () => {
      if (currentChat.messages.length > 0) {
        const lastUserMessage = currentChat.messages.filter(msg => msg.role === 'user').pop();
        if (lastUserMessage) {
          userInput.value = lastUserMessage.content;
          sendMessage();
        }
      }
    };
    
    // 获取用户名首字母（用于用户头像显示）
    const getInitials = (userId) => {
      if (!userId) return '👤';
      // 简单处理：如果userId是数字，返回前两位
      if (!isNaN(userId)) {
        return userId.toString().slice(0, 2);
      }
      // 如果是字符串，返回首字母
      return userId.charAt(0).toUpperCase();
    };
    
    // 处理退出登录
    const handleLogout = async () => {
      if (confirm(language.value === 'zh' ? '确定要退出登录吗？' : 'Are you sure you want to log out?')) {
        try {
          // 调用退出登录函数
          const success = await logout(true);
          
          if (success) {
            // 清除用户信息
            currentUserInfo.value = null;
            
            // 清空当前对话
            currentChat.messages = [];
            
            // 显示登录对话框
            showLoginDialog.value = true;
            
            console.log('用户已成功退出登录');
          } else {
            console.error('退出登录失败');
          }
        } catch (error) {
          console.error('退出登录过程中发生错误:', error);
        }
      }
    };

    // 点赞消息
    const likeMessage = (index) => {
      // 实现点赞功能
      console.log('点赞消息:', index);
    };

    // 删除消息
    const deleteMessage = (index) => {
      if (confirm('确定要删除这条消息吗？')) {
        currentChat.messages.splice(index, 1);
        // 保存更新后的聊天记录
        saveChatHistory();
        console.log('删除消息:', index);
      }
    };

    // 关闭登录对话框
    const closeLoginDialog = () => {
      showLoginDialog.value = false;
    };

    // 导出对话功能已移除

    // 加载更多聊天记录
    const loadMoreChats = () => {
      // 实现加载更多功能
      console.log('加载更多聊天记录');
    };

    return {
      language,
      userInput,
      loading,
      chatHistory,
      currentChat,
      currentChatIndex,
      currentChatTitle,
      isInThinkingMode,
      thinkingContent,
      showThinking,
      exampleQuestions,
      startNewChat,
      loadChat,
      changeLanguage,
      sendExampleQuestion,
      sendMessage,
      formatMessage,
      chatMessagesContainer,
      clearInput,
      deleteChat,
      formatTime,
      formatDate,
      copyMessage,
      copyInput,
      regenerateResponse,
      likeMessage,
      deleteMessage,
      loadMoreChats,
      cancelRequest,
      showLoginDialog,
      closeLoginDialog,
      loginType,
      loginUsername,
      loginPassword,
      loginError,
      loginLoading,
      handleLogin,
      currentUserInfo,
      getInitials,
      handleLogout
